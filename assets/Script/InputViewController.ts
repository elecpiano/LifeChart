    FinishInput(){
        // console.log("xxx " + this.inputBox.string);

        let param = this.inputBox.string.trim();
        let paramArr = param.split(" ");

        let hanzi = "";
        let pinyin = "";

        let valid = false;
        if (paramArr.length == 2) {
            valid = true;

            // if hanzi, try convert pinyin
            if (!this.IsPinyin(param[0])) {
                for (let i = 0; i < paramArr[0].length; i++) {
                    let pyChar = this.GetPinyin(paramArr[0][i]);
                    if (pyChar != "") {
                        pinyin += pyChar;
                        hanzi += paramArr[0][i];
                    }
                    else{
                        this.notice.string = "无法识别汉字，请用拼音重新输入";
                        valid = false;
                        break;
                    }
                }
            }
            else{ // if pinyin, check all pinyin
                for (let i = 0; i < paramArr[0].length; i++) {
                    if (this.IsPinyin(paramArr[0][i])) {
                        pinyin += paramArr[0][i];
                        hanzi += paramArr[0][i];
                    }
                    else{
                        this.notice.string = "拼音输入有误，请重新输入";
                        valid = false;
                        break;
                    }
                }
            }

            //check birthday
            if (paramArr[1].length == 8) {
                for (let i = 0; i < paramArr[1].length; i++) {
                    if (!this.IsDigit(paramArr[1][i])) {
                        this.notice.string = "生日输入有误，请重新输入";
                        valid = false;
                        break;
                    }
                }
            }
            else{
                valid = false;
                this.notice.string = "生日输入有误，请重新输入";
            }
        }
        else{
            valid = false;
            this.notice.string = "请按“姓名(空格)19910215”的格式输入";
        }

        if (valid) {
            param = hanzi + " " + pinyin + " " + paramArr[1];

            // console.log("xxx valid : " + param);

            if (!this.GlobalData.Compare) {
                this.GlobalData.ParamList.splice(0,this.GlobalData.ParamList.length);                
            }
            this.GlobalData.ParamList.push(param);
            cc.director.loadScene("MainView");
        }
        else {
            this.inputBox.string = "";
            this.notice.node.opacity = 255;
            this.notice.node.runAction(cc.sequence(
                cc.scaleTo(this.NOTICE_DURATION * 0.3, 1.1, 1.1),
                cc.scaleTo(this.NOTICE_DURATION * 0.7, 1.0, 1.0)
            ));
            
            // this.node.runAction(cc.sequence(
            //     cc.delayTime(0.7),
            //     cc.callFunc(()=>this.SetFocus())
            // ));
        }
    }

