    GetPinyin(hanzi:string) : string {
        let pinyin = "";

        let idx = 0;
        let found = false;
        while (idx < this.PinyinDictionary.length) {
            let str = this.PinyinDictionary[idx];
            if (str == hanzi) {
                found = true;
            }
            if (found) {
                if (str[0] == ":") {
                    pinyin = str.replace(":","");
                    break;
                }
            }
            idx++;
        }
        
        return pinyin;
    }

    FixPinYin(target:string) : string {
        let fix = target;
        if (target == "ā" || target == "á" || target == "ǎ" || target == "à") {
            fix = "a";
        }
        else if (target == "ē" || target == "é" || target == "ě" || target == "è") {
            fix = "e";
        }
        else if (target == "ō" || target == "ó" || target == "ǒ" || target == "ò") {
            fix = "o";
        }
        else if (target == "ī" || target == "í" || target == "ǐ" || target == "ì") {
            fix = "i";
        }
        else if (target == "ū" || target == "ú" || target == "ǔ" || target == "ù") {
            fix = "u";
        }
        else if (target == "ü" || target == "ǘ" || target == "ǚ" || target == "ǜ") {
            fix = "u";
        }
        else if (target == "ń" || target == "ň") {
            fix = "n";
        }
        else if (target == "") {
            fix = "m";
        }
        return fix;
    }

